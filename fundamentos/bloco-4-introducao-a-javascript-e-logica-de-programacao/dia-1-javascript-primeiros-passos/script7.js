let value = 50;
let result;

if (value < 50) {
    result = "F";
} else if (value < 60) {
    result = "E";
} else if (value < 70) {
    result = "D";
} else if (value < 80) {
    result = "C";
} else if (value < 90) {
    result = "B";
} else if (value <= 100) {
    result = "A";
} 

console.log(result);